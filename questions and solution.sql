-- question 7
-- Identify the top 3 suppliers based on the total revenue generated by their products.

select s.id as supplierid, s.contactname, sum(oi.quantity* oi.unitprice) as total_revenue
from supplier s
join product p on p.supplierid=s.id
join orderitem oi on oi.productid=p.id
join orders o on o.id= oi.orderid
group by s.id,s.contactname
order by total_revenue desc
limit 3;


