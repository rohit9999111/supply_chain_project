-- Section D: Level 4 (Expert Queries)
-- Savings Analysis: The company offers discounts. Calculate the total amount saved in each order (Actual Price - Selling Price) and rank orders by total savings.


select oi.orderid, sum((p.unitprice- oi.unitprice)*oi.quantity) as total_saving
from product p 
join orderitem oi on oi.productid =p.id
group by oi.orderid
order by total_saving desc;

-- question 2 
-- Market Research: Help a new supplier, Mr. Kavin, by identifying:
-- a. A list of high-demand products he should consider supplying.
-- b. A list of his potential competitors for those products.

-- a A list of high-demand products he should consider supplying.
select p.productname, sum(oi.quantity) as total_demand
from product p 
join orderitem oi on oi.productid=p.id
group by p.productname
order by total_demand desc;

-- b  A list of his potential competitors for those products.

select distinct s.contactname ,p.productname
from orderitem oi
join product p on p.id= oi.productid
join supplier s on p.supplierid = s.id;
