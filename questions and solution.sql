-- 6 Display the costliest item that is ordered by the customer  --
-- SELECT c.firstname, max(p.unitprice) as maxprice
-- from customer c 
-- join product p
-- on c.id = p.id
-- group by firstname
-- order by maxprice desc;

SELECT c.id AS customer_id, p.productname, p.unitprice 
FROM customer c 
JOIN orders o ON c.id = o.customerid
JOIN orderitem oi ON o.id = oi.orderid
JOIN product p ON p.id = oi.productid
WHERE (c.id, p.unitprice) IN (
    SELECT c.id, MAX(p.unitprice) 
    FROM product p
    JOIN orderitem oi ON oi.productid = p.id
    JOIN orders o ON o.id = oi.orderid
    JOIN customer c ON o.customerid = c.id
    GROUP BY c.id
);

-- question 7 Top Suppliers: Find the Supplier ID that provides the highest number of products.
with max_p as(select s.id, count(p.id) as cnt
from supplier s
join product p
on p.supplierid=s.id
group by s.id
)
select * from max_p
where cnt in(select max(cnt) from max_p);

